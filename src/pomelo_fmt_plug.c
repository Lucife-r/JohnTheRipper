static void char_to_bin(char *in, int char_length, char *bin)
{
	int i;
	for (i = 0; i < char_length; i += 2) {
		char a = in[i];
		char b = in[i + 1];
		if (a >= 60)
			a -= 55;
		else
			a -= 48;
		if (b >= 60)
			b -= 55;
		else
			b -= 48;
		bin[i / 2] = a << 4;
		bin[i / 2] += b;
	}
}

static void init(struct fmt_main *self)
{
#ifdef _OPENMP
	int omp_t = omp_get_max_threads();
	self->params.min_keys_per_crypt *= omp_t;
	omp_t *= OMP_SCALE;
	self->params.max_keys_per_crypt *= omp_t;
#endif
	saved_key =
	    malloc((self->params.max_keys_per_crypt+3) * (PLAINTEXT_LENGTH + 1));
	memset(saved_key, 0,
	    (self->params.max_keys_per_crypt+3) * (PLAINTEXT_LENGTH + 1));

	saved_length =
	    malloc((self->params.max_keys_per_crypt+3) * sizeof(size_t));
	memset(saved_length, 0,
	    (self->params.max_keys_per_crypt+3) * sizeof(size_t));

	crypted = malloc((self->params.max_keys_per_crypt+3) * (BINARY_SIZE));
	memset(crypted, 0, (self->params.max_keys_per_crypt+3) * (BINARY_SIZE));
}

static void done(void)
{
	free(saved_key);
	free(saved_length);
	free(crypted);
}

static int valid(char *ciphertext, struct fmt_main *self)
{
	char *next_dollar;
	char *i;

	if (strncmp(ciphertext, "$pomelo$", 8) &&
	    strncmp(ciphertext, "$POMELO$", 8))
		return 0;
	i = ciphertext + 8;
	//t_cost
	next_dollar = strchr(i, '$');
	if (next_dollar == NULL || next_dollar - i > 4 || next_dollar == i)
		return 0;
	i = next_dollar + 1;
	//m_cost
	next_dollar = strchr(i, '$');
	if (next_dollar == NULL || next_dollar - i > 2 || next_dollar == i)
		return 0;
	i = next_dollar + 1;
	//salt
	next_dollar = strchr(i, '$');
	if (next_dollar == NULL || next_dollar - i > SALT_SIZE ||
	    next_dollar == i)
		return 0;
	i = next_dollar + 1;
	if (strlen(i) > 512 || strlen(i) == 0)
		return 0;
	while (atoi16[ARCH_INDEX(*i)] != 0x7F)	//
		i++;
	if (*i)
		return 0;
	return 1;
}

static void *get_binary(char *ciphertext)
{
	char *ii;
	static char out[BINARY_SIZE];
	memset(out, 0, BINARY_SIZE);

	ii = strrchr(ciphertext, '$');
	ii = ii + 1;
	out[0] = strlen(ii) / 2;
	char_to_bin(ii, strlen(ii), out + 1);
	return out;
}



